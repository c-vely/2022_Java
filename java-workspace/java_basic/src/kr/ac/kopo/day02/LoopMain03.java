public class LoopMain03 {
	
	public static void main(String[] args) {
		
		//i를 횟수로 쓸 때
		for(int i = 1; i <= 5; i++){
		System.out.printf("%d", 6-i);
		}
		System.out.println();
		
		
		//i를 정수로 쓸 대
		for(int i = 5; i >= 1; i--) {
			System.out.printf("%d", i);
		}
	}

}
